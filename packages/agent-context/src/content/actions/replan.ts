import type { ToolTarget } from '../../types.js'

export function renderReplan(target: ToolTarget): string {
  return `# replan

重新规划已有的未实施计划，保持「单当前计划 + preparing 队列」结构不变。

必须附带重规划描述。

## 前置检查

- 在 shell 中运行 \`agent-context validate\`，若不通过则根据错误信息修正对应内容（如修复状态行格式、补全缺失文件等），修正后重新运行验证，重复直至通过。
- 描述为空 → 向用户获取重规划描述后继续执行。
- 无未实施计划 → 通过 ${target.askToolName} 提供选项：1) 创建新计划 2) 终止操作，按用户选择执行。

## 作用域

- 固定覆盖**全部**未执行计划，不支持部分重规划。
- 未执行计划包括：当前计划（当且仅当其状态为 \`未执行\`）+ \`.agent-context/{scope}/preparing/\` 中全部计划。
- 当前计划为 \`已执行\` → 当前计划不纳入重规划；仅对 \`preparing/\` 队列中的计划进行重规划。

## 执行步骤

1. 结合描述确定重规划方向。若重规划方向仍不明确（拆分方式、优先级调整、范围增减等），通过 ${target.askToolName} 提问澄清后再继续。
2. **反向面试**：分析原计划需要调整的根本原因，评估新方向是否真正解决了问题。关注：重规划的核心驱动因素（技术障碍 / 需求变更 / 设计缺陷 / 优先级调整）、新方案是否引入原计划不存在的风险或约束。通过 ${target.askToolName} 提出关键探测问题。若分析后无显著问题可跳过。
3. 读取上述作用域内**所有**未执行计划的 \`plan.md\`，理解现有意图。
4. 生成新的拆分方案，保持「单当前计划 + 若干 preparing 计划」结构。
5. 新增计划编号：全局 max(N)+1 递增分配；未改动计划保持原编号。
6. 更新目录结构，确保每个新计划的 \`plan.md\` 遵循标准模板。
`
}
