import type { ToolTarget } from '../../types.js'

export function renderPlan(_target: ToolTarget): string {
  return `# plan

创建新的执行计划，写入 \`.agent-context/{scope}/plan-{number}/plan.md\`。按复杂度可拆分为多个计划，维护「单当前计划 + preparing 队列」结构。

必须附带计划描述。

## 前置检查

- 运行 \`agent-context validate\`，若不通过则根据错误信息修正对应内容（如修复状态行格式、补全缺失文件等），修正后重新运行验证，重复直至通过。
- 描述为空 → 向用户获取描述后继续执行。
- 存在未归档的已执行当前计划 → 通过 AskUserQuestion 提供选项：1) 运行 \`agent-context done\` 归档后继续创建计划（推荐） 2) 终止操作，按用户选择执行。
- 存在未实施的当前计划（状态为 \`未执行\`） → 通过 AskUserQuestion 提供选项：1) 通过 replan 调整现有计划（推荐，若新需求与现有计划相关） 2) 归档现有计划后创建新计划 3) 终止操作，按用户选择执行。
- 存在多个当前计划 → 通过 AskUserQuestion 列出所有当前计划供用户选择保留哪个，清理后继续执行。

## 执行步骤

1. **需求澄清**（存在以下任一歧义时通过 AskUserQuestion 提问，否则跳到步骤 2）：
   - 范围边界不清：无法判定影响哪些文件或模块。
   - 存在显著不同的技术路径需用户决策。
   - 验收标准不明确：无法判断何时算"完成"。
   - 涉及架构设计或文档编写但具体内容未明确（如"编写 XX 架构文档"但未说明包含哪些具体内容）。
2. 按复杂度决定单计划或多计划拆分。
3. 多计划拆分时：最小编号进入 \`.agent-context/{scope}/\` 作为当前计划，其余进入 \`.agent-context/{scope}/preparing/\`。单计划直接创建。
4. 每个计划创建 \`plan.md\`，遵循下方模板。
5. **自检**（不通过则修改后重新检查）：
   - 每个步骤可独立执行且有明确完成标准。
   - 不存在过度拆分或拆分不足。
   - 影响范围可预估。
   - **无模糊指令检查**：逐条审查 \`## 内容\` 中的每个步骤，确保不存在以下问题：
     - 步骤仅给出方向性描述而缺少具体实施细节（如"编写 XX 文档，包含 XX、XX、XX"但未说明每项的具体内容和标准）。
     - 步骤中包含"等"、"其他"、"相关"等开放性词汇导致边界不清。
     - 步骤依赖尚未确定的技术选型或设计决策。
     - 步骤的产出物格式、内容结构未明确定义。
   - 若发现模糊指令 → 通过 AskUserQuestion 向用户澄清具体内容后修正计划，不可带着模糊内容生成计划。

## plan.md 模板

\`\`\`markdown
# {计划名称}

> 状态: 未执行

## 目标

{明确的目标描述}

## 内容

{详细的实施步骤}

## 影响范围

## 历史补丁
\`\`\`

- 状态行唯一，仅允许 \`未执行\` 或 \`已执行\`。
- \`## 目标\` 与 \`## 内容\` 不可为空。
- \`## 影响范围\` 与 \`## 历史补丁\` 创建时留空，后续由 implement 和 patch 写入。
`
}
