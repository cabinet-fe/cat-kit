import type { ToolTarget } from '../../types.js'

export function renderInit(target: ToolTarget): string {
  const guide = target.guideFileName
  return `# init

初始化项目的 Agent Context 上下文。根据新旧项目类型处理 \`${guide}\`，新项目会自动进入计划创建流程。

可附带描述参数，用于补充项目背景、技术栈、特殊约束。

## 前置检查

- 若已存在 \`.agent-context/\`：在 shell 中运行 \`agent-context validate\`，若不通过则根据错误信息修正对应内容（如修复状态行格式、补全缺失文件等），修正后重新运行验证，重复直至通过。
- 若 \`.agent-context/\` 不存在：确认这是首次初始化场景；若目录残缺或状态异常，向用户报告，不可继续假设。
- 新项目缺少关键决策信息时，必须通过 ${target.askToolName} 主动提问或确认，不可直接生成 \`${guide}\` 或进入 plan。
- 遇到阻塞问题应通过 \`${target.askToolName}\` 工具向用户报告并请求决策，不可静默跳过。

## 执行步骤

1. **判断项目类型**：检测根目录是否存在有效代码文件和目录结构。
   - 新项目：无实质代码，或用户明确说明是新项目。
   - 旧项目：已有代码、配置文件和目录结构。
   - 无法判断 → 通过 ${target.askToolName} 向用户确认项目类型，不可假设。

2. **处理 ${guide}**：
   - 新项目：
     1. 通过 ${target.askToolName} **分轮次**收集以下信息。每轮聚焦一个主题，避免单次提问过多导致用户遗漏关键决策。前三类属于**必须主动确认**的决策项，即使描述已部分涵盖也不能整体跳过；可基于描述用确认式问题补问，而不是完全重问：
        - 必须主动确认：**项目目标与产品定位**——项目要解决什么问题、面向什么用户群体、核心功能是什么、产品亮点或差异化价值是什么
        - 必须主动确认：**项目架构**（目录结构、分层模式、关键框架或方案选型）
        - 必须主动确认：**技术栈与版本**（语言、运行时、框架、构建/测试工具等）
        - 可基于描述补充：**开发偏好与实施路径**——用户倾向完美主义（高质量、充分设计后再动手）还是快速原型验证（先跑通再迭代），这将影响计划的粒度和实施策略
        - 可基于描述补充：代码规范与工具链细节（lint、formatter、测试约定等）
     2. 对高价值决策类问题必须在计划前通过 ${target.askToolName} 澄清，例如：是否使用 monorepo、前后端如何拆分、采用哪个框架与测试工具。
     3. **竞品参考提醒**：通过 ${target.askToolName} 询问用户是否需要搜索竞品或同类项目信息，以了解差异化方向和可借鉴的设计。若用户同意，使用 WebSearch 搜索相关竞品信息并整理关键差异点供用户参考后再继续。
     4. **反向面试**：基于前几轮收集的全部信息，按 ${target.askToolName} 规范中的反向面试方法，主动审视用户的回答，构造 2-3 个探测性问题。此环节的问题必须针对用户的具体描述构造，不可使用泛化的检查清单。若审视后未发现显著盲区可跳过。
     5. 生成高质量 \`${guide}\`（须满足 **## 高质量 ${guide} 标准**，不满足则重新优化直至满足）。
     6. 继续执行 plan 创建初始计划。
   - 旧项目：
     1. 若不存在 \`${guide}\`：优先通过代码与配置读取收集信息；仅对无法可靠判断的内容通过 \`${target.askToolName}\` 提问后生成。
     2. 若已存在 \`${guide}\`：按质量标准评估，不足时增补优化。
     3. 默认不创建计划（除非用户明确要求）。

3. **输出反馈**：向用户报告项目类型判定结果、${guide} 处理结果、是否需要后续计划。

## 高质量 ${guide} 标准

- 常用命令与约束前置，降低代理执行歧义。
- 明确列出目录结构（至少到第二层）。
- 明确标注技术栈及版本。
- 明确代码风格约束（命名规范、格式化工具、lint 配置）。
- 内容精简，无冗余段落或模板化填充。
- 大型单体仓库按子包拆分维护本地 \`${guide}\`。
`
}
