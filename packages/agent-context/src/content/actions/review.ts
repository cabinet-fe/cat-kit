import type { ToolTarget } from '../../types.js'

export function renderReview(target: ToolTarget): string {
  const guide = target.guideFileName
  return `# review

对当前计划进行独立审查。

不接受额外描述。

## 前置检查

- 运行 \`agent-context validate\`，若不通过则根据错误信息修正对应内容（如修复状态行格式、补全缺失文件等），修正后重新运行验证，重复直至通过。
- 当前计划不存在 → 提示无可审查内容，终止。
- 存在多个当前计划 → 通过 ${target.askToolName} 列出所有当前计划供用户选择保留哪个，清理后继续执行。

## 执行步骤

1. **读取计划**：读取当前计划的 \`plan.md\` 和所有 \`patch-*.md\`，确认状态（未执行/已执行）。

2. **读取项目指导文件**：读取项目根目录的 \`${guide}\`。

3. **按状态分支**：

   - **IF 未执行**（计划审查）：
     - 使用**独立子代理**（Agent 工具）进行审查，防止上下文共享导致审查失效。
     - 子代理 prompt 包含：计划全文、项目指导文件全文、审查清单。
     - 审查清单：
       - 目标是否明确可衡量
       - 步骤是否完整无遗漏无冗余
       - 步骤间依赖关系是否合理
       - 技术方案是否符合项目指导文件的约束
       - 影响范围预估是否合理
       - 是否存在潜在风险或遗漏边界情况
       - **是否存在模糊指令**：逐条检查每个步骤是否包含方向性描述而缺少具体实施细节、是否使用"等"/"其他"/"相关"等开放性词汇导致边界不清、是否依赖尚未确定的技术选型或设计决策、产出物的格式和内容结构是否明确定义

   - **ELSE IF 已执行**（代码审查）：
     - 读取 \`## 影响范围\` 获取变更文件列表。
     - 使用 \`git diff\` 获取这些文件的具体变更内容。
     - 使用**独立子代理**（Agent 工具）进行审查。
     - 子代理 prompt 包含：计划全文（含补丁）、项目指导文件全文、变更文件 diff、审查清单。
     - 审查清单：
       - 实现是否完整覆盖计划所有步骤
       - 代码质量是否符合项目指导文件中的规范（命名、风格、约束）
       - 是否引入不必要的副作用或破坏性变更
       - 是否遗漏测试或文档更新
       - 是否存在安全隐患或性能问题
       - 是否存在过度设计或实现不足

4. **汇总审查结果**：将子代理返回的审查意见整理呈现给用户。

5. **提供行动选项**：通过 ${target.askToolName} 提供选项：
   - 未执行计划：1) 按审查意见修订计划（走 replan）（推荐） 2) 忽略审查意见继续执行 3) 终止操作
   - 已执行计划：1) 按审查意见创建补丁修复（走 patch）（推荐） 2) 忽略审查意见 3) 终止操作

## 子代理隔离要求

- 明确要求使用 Agent 工具启动子代理。
- 子代理 prompt 必须包含完整上下文（计划、指导文件、diff），不依赖对话历史。
- 子代理以**独立第三方审查员**身份执行，审查视角独立于原始实施者。
- 子代理返回结构化审查报告：问题列表（严重程度 + 描述 + 建议修复方式）。
`
}
