import type { ToolTarget } from '../../types'

export function renderReview(target: ToolTarget): string {
  const guide = target.guideFileName
  const ask = target.askToolName
  return `# review

对计划进行独立审查。支持审查当前计划和 preparing 队列中的计划，自动根据计划状态选择审查方式。

可选附带审查范围描述（如指定审查某个具体计划）。

## 前置检查

- 在 shell 中运行 \`agent-context validate\`，若不通过则根据错误信息修正对应内容（如修复状态行格式、补全缺失文件等），修正后重新运行验证，重复直至通过。
- 无当前计划且 preparing 队列为空 → 提示无可审查内容，终止。
- **禁止审查已归档（done）计划**。若用户要求审查已归档计划 → 拒绝并说明：已归档计划不可审查，如需回顾可直接阅读归档目录下的 plan.md。
- 存在多个当前计划 → 通过 ${ask} 列出所有当前计划供用户选择保留哪个，清理后继续执行。

## 执行步骤

1. **确定审查范围**：

   - **IF 用户明确指定了审查对象** → 按指定执行（仍须校验目标不在 done 目录）。
   - **ELSE IF 存在 preparing 计划且存在当前计划** → 通过 ${ask} 引导选择审查范围：
     1. 仅当前计划
     2. 当前计划 + preparing 队列中的计划（推荐，全面审查待执行序列）
     3. 所有未执行计划（仅含状态为「未执行」的计划，已执行的当前计划不包含在内）
   - **ELSE IF 存在 preparing 计划但无当前计划** → 直接审查 preparing 中的全部计划。
   - **ELSE**（仅当前计划，无 preparing）→ 直接审查当前计划。

2. **询问子代理模式**：通过 ${ask} 询问是否启动独立子代理执行审查：
   1. 不使用子代理，在当前会话中审查（推荐，适用于新开会话或上下文较短的场景）
   2. 启动独立子代理审查（适用于已有较长对话历史的会话，避免上下文污染影响审查独立性）

3. **读取项目指导文件**：读取项目根目录的 \`${guide}\`。

4. **逐计划审查**：对审查范围内的每个计划执行以下子步骤：

   a. **读取计划**：读取该计划的 \`plan.md\` 和所有 \`patch-*.md\`，确认状态（未执行/已执行）。

   b. **按状态分支审查**：

      - **IF 未执行**（计划审查）：
        - 审查清单：
          - 目标是否明确可衡量
          - 步骤是否完整无遗漏无冗余
          - 步骤间依赖关系是否合理
          - 技术方案是否符合项目指导文件的约束
          - 影响范围预估是否合理
          - 是否存在潜在风险或遗漏边界情况
          - **是否存在模糊指令**：逐条检查每个步骤是否包含方向性描述而缺少具体实施细节、是否使用"等"/"其他"/"相关"等开放性词汇导致边界不清、是否依赖尚未确定的技术选型或设计决策、产出物的格式和内容结构是否明确定义

      - **ELSE IF 已执行**（代码审查）：
        - 读取 \`## 影响范围\` 获取变更文件列表。
        - 使用 \`git diff\` 获取这些文件的具体变更内容。
        - 审查清单：
          - 实现是否完整覆盖计划所有步骤
          - 代码质量是否符合项目指导文件中的规范（命名、风格、约束）
          - 是否引入不必要的副作用或破坏性变更
          - 是否遗漏测试或文档更新
          - 是否存在安全隐患或性能问题
          - 是否存在过度设计或实现不足

   c. **执行审查**：
      - **IF 选择子代理模式** → 启动独立子代理（Agent 工具），prompt 包含：计划全文（含补丁）、项目指导文件全文、变更文件 diff（仅已执行计划）、对应审查清单。子代理以独立第三方审查员身份执行，返回结构化审查报告。
      - **ELSE** → 在当前会话中按审查清单逐项检查，整理审查意见。

5. **汇总审查结果**：整理所有计划的审查意见呈现给用户。

6. **提供行动选项**：根据审查结果，通过 ${ask} 提供下一步操作选项：
   - 未执行计划：1) 按审查意见修订计划（走 replan）（推荐） 2) 忽略审查意见继续执行 3) 终止操作
   - 已执行计划：1) 按审查意见创建补丁修复（走 patch）（推荐） 2) 忽略审查意见 3) 终止操作
   - 混合场景（审查范围同时包含未执行和已执行计划）：分别列出各计划的状态与建议操作供用户逐一选择

## 子代理隔离要求（仅在选择子代理模式时适用）

- 明确要求使用 Agent 工具启动子代理。
- 子代理 prompt 必须包含完整上下文（计划、指导文件、diff），不依赖对话历史。
- 子代理以**独立第三方审查员**身份执行，审查视角独立于原始实施者。
- 子代理返回结构化审查报告：问题列表（严重程度 + 描述 + 建议修复方式）。
`
}
