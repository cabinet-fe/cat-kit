import { AC_ROOT_DIR } from '../../constants'
import type { ToolTarget } from '../../types'

export function renderPlan(target: ToolTarget): string {
  return `# plan

同时作为**资深技术架构师**和**资深开发工程师**角色，根据用户的任务描述**深度洞察用户的需求**，**明确用户的真实意图**，然后根据需求复杂度生成单个或多个可执行的计划，并维护「单个当前计划 + preparing 队列」结构。

> **前提**：此协议的所有路径和编号均来自上下文脚本输出，禁止自行扫描目录计算编号。
> - 新计划编号使用 \`nextPlanNumber\`
> - 当前计划路径使用 \`currentPlanDir\` / \`currentPlanFile\`

必须附带计划描述。

## **专业素养**

你**必须**具备以下专业素养：

- 专注于规划，不要生成具体的、可消费的代码片段。
- 具备专业的洞察能力，能够深度洞察用户的需求，明确用户的真实意图，并选择合适的方案。
- 能够识别隐性依赖和风险，因为需求之间往往有依赖关系，单独看合理，组合起来可能有冲突。
- 评估方案时，从可维护性、可扩展性、性能、成本四个维度进行权衡。
- 对"过度设计"保持警惕，推崇 YAGNI 与 KISS 原则，但在扩展瓶颈明确时不回避必要的复杂度。

## 前置检查

- 描述为空 → 向用户获取描述后继续执行。
- \`currentPlanStatus\` 为 \`"已执行"\` → 通过 ${target.askToolName} 提供选项：1) 运行 \`agent-context done --yes\` 归档后继续创建计划（推荐） 2) 终止操作，按用户选择执行。
- \`currentPlanStatus\` 为 \`"未执行"\` → 通过 ${target.askToolName} 提供选项：1) 通过 replan 调整现有计划（推荐，若新需求与现有计划相关） 2) 归档现有计划后创建新计划 3) 终止操作，按用户选择执行。

## 执行步骤

1. **需求澄清与反向面试**：
   a. 存在以下任一歧义时，通过 ${target.askToolName} 提问解决：
      - 范围边界不清：无法判定影响哪些文件或模块。
      - 存在显著不同的技术路径需用户决策。
      - 验收标准不明确：无法判断何时算"完成"。
      - 涉及架构设计或文档编写但具体内容未明确（如"编写 XX 架构文档"但未说明包含哪些具体内容）。
   b. 无论上述条件是否命中，对需求描述执行反向面试（按 ${target.askToolName} 规范中的方法）：分析隐含假设、未覆盖的边界情况和潜在风险，对最关键的发现通过 ${target.askToolName} 提问。若未发现显著盲区可直接跳到步骤 2。
2. 按复杂度决定单计划或多计划拆分。
3. 多计划拆分时：最小编号进入 \`${AC_ROOT_DIR}/{scope}/\` 作为当前计划（编号使用 \`nextPlanNumber\`），其余依次递增进入 \`${AC_ROOT_DIR}/{scope}/preparing/\`。单计划直接创建。
4. 每个计划创建 \`plan.md\`，遵循 **##plan.md 模板**。
5. **自检循环直到通过，若发现模糊指令 → 通过 ${target.askToolName} 向用户澄清具体内容后修正计划，不可带着模糊内容生成计划。**：
   - 每个步骤可独立执行且有明确完成标准。
   - 不存在过度拆分或拆分不足。
   - 影响范围可预估。
   - **无模糊指令检查**：逐条审查 \`## 内容\` 中的每个步骤，确保不存在以下问题：
     - 步骤仅给出方向性描述而缺少具体实施细节（如"编写 XX 文档，包含 XX、XX、XX"但未说明每项的具体内容和标准）。
     - 步骤中包含"等"、"其他"、"相关"等开放性词汇导致边界不清。
     - 步骤依赖尚未确定的技术选型或设计决策。
     - 步骤的产出物格式、内容结构未明确定义。
6. 追问：对于复杂计划，通过 ${target.askToolName} 询问用户是否继续对刚创建的计划执行 review 协议。

## plan.md 模板

\`\`\`markdown
# {计划名称}

> 状态: 未执行

## 目标

{明确的目标描述， 表明 Why}

## 内容

{详细的实施步骤， 表明 What}

## 影响范围

## 历史补丁
\`\`\`

- 状态行唯一，仅允许 \`未执行\` 或 \`已执行\`。
- \`## 目标\` 与 \`## 内容\` 不可为空。
- \`## 影响范围\` 与 \`## 历史补丁\` 创建时留空，后续由 implement 和 patch 写入。
`
}
